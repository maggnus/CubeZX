struct AESCrypto {
    private let key: [UInt8]
    private let expandedKey: [UInt8]
    
    init(key: [UInt8]) {
        self.key = key
        self.expandedKey = Self.expandKey(key)
    }
    
    init(key: Data) {
        self.init(key: Array(key))
    }
    
    func decrypt(_ data: Data) -> Data? {
        var result = Data()
        for i in stride(from: 0, to: data.count, by: 16) {
            let end = min(i + 16, data.count)
            let block = Array(data[i..<end])
            guard block.count == 16, let decrypted = decryptBlock(block) else { continue }
            result.append(decrypted)
        }
        return result.isEmpty ? nil : result
    }
    
    func encrypt(_ data: Data) -> Data? {
        var result = Data()
        for i in stride(from: 0, to: data.count, by: 16) {
            let end = min(i + 16, data.count)
            var block = Array(data[i..<end])
            while block.count < 16 { block.append(0) }
            if let encrypted = encryptBlock(block) {
                result.append(contentsOf: encrypted)
            }
        }
        return result
    }
    
    private func decryptBlock(_ block: [UInt8]) -> Data? {
        var state = block
        
        addRoundKey(&state, 10)
        invShiftRows(&state)
        invSubBytes(&state)
        
        for round in stride(from: 9, through: 1, by: -1) {
            addRoundKey(&state, round)
            invMixColumns(&state)
            invShiftRows(&state)
            invSubBytes(&state)
        }
        
        addRoundKey(&state, 0)
        
        return Data(state)
    }
    
    private func encryptBlock(_ block: [UInt8]) -> [UInt8]? {
        var state = block
        
        addRoundKey(&state, 0)
        
        for round in 1..<10 {
            subBytes(&state)
            shiftRows(&state)
            mixColumns(&state)
            addRoundKey(&state, round)
        }
        
        subBytes(&state)
        shiftRows(&state)
        addRoundKey(&state, 10)
        
        return state
    }
    
    private func addRoundKey(_ state: inout [UInt8], _ round: Int) {
        for i in 0..<16 {
            state[i] ^= expandedKey[round * 16 + i]
        }
    }
    
    private func subBytes(_ state: inout [UInt8]) {
        for i in 0..<16 {
            state[i] = Self.sbox[Int(state[i])]
        }
    }
    
    private func invSubBytes(_ state: inout [UInt8]) {
        for i in 0..<16 {
            state[i] = Self.invSbox[Int(state[i])]
        }
    }
    
    private func shiftRows(_ state: inout [UInt8]) {
        let s = state
        state = [s[0], s[5], s[10], s[15], s[4], s[9], s[14], s[3],
                 s[8], s[13], s[2], s[7], s[12], s[1], s[6], s[11]]
    }
    
    private func invShiftRows(_ state: inout [UInt8]) {
        let s = state
        state = [s[0], s[13], s[10], s[7], s[4], s[1], s[14], s[11],
                 s[8], s[5], s[2], s[15], s[12], s[9], s[6], s[3]]
    }
    
    private func mixColumns(_ state: inout [UInt8]) {
        var result = [UInt8](repeating: 0, count: 16)
        for i in 0..<4 {
            let col = [state[i*4], state[i*4+1], state[i*4+2], state[i*4+3]]
            result[i*4]   = gmul(col[0], 0x02) ^ gmul(col[1], 0x03) ^ col[2] ^ col[3]
            result[i*4+1] = col[0] ^ gmul(col[1], 0x02) ^ gmul(col[2], 0x03) ^ col[3]
            result[i*4+2] = col[0] ^ col[1] ^ gmul(col[2], 0x02) ^ gmul(col[3], 0x03)
            result[i*4+3] = gmul(col[0], 0x03) ^ col[1] ^ col[2] ^ gmul(col[3], 0x02)
        }
        state = result
    }
    
    private func invMixColumns(_ state: inout [UInt8]) {
        var result = [UInt8](repeating: 0, count: 16)
        for i in 0..<4 {
            let col = [state[i*4], state[i*4+1], state[i*4+2], state[i*4+3]]
            result[i*4]   = gmul(col[0], 0x0e) ^ gmul(col[1], 0x0b) ^ gmul(col[2], 0x0d) ^ gmul(col[3], 0x09)
            result[i*4+1] = gmul(col[0], 0x09) ^ gmul(col[1], 0x0e) ^ gmul(col[2], 0x0b) ^ gmul(col[3], 0x0d)
            result[i*4+2] = gmul(col[0], 0x0d) ^ gmul(col[1], 0x09) ^ gmul(col[2], 0x0e) ^ gmul(col[3], 0x0b)
            result[i*4+3] = gmul(col[0], 0x0b) ^ gmul(col[1], 0x0d) ^ gmul(col[2], 0x09) ^ gmul(col[3], 0x0e)
        }
        state = result
    }
    
    private func gmul(_ a: UInt8, _ b: UInt8) -> UInt8 {
        var p: UInt8 = 0
        var aa = a
        var bb = b
        for _ in 0..<8 {
            if bb & 1 != 0 { p ^= aa }
            let hi = aa & 0x80
            aa = aa << 1
            if hi != 0 { aa ^= 0x1b }
            bb = bb >> 1
        }
        return p
    }
    
    private static func expandKey(_ key: [UInt8]) -> [UInt8] {
        var w = key
        for i in 4..<44 {
            var temp = Array(w[(i-1)*4..<i*4])
            if i % 4 == 0 {
                temp = [sbox[Int(temp[1])] ^ rcon[i/4 - 1],
                        sbox[Int(temp[2])],
                        sbox[Int(temp[3])],
                        sbox[Int(temp[0])]]
            }
            for j in 0..<4 {
                w.append(w[(i-4)*4 + j] ^ temp[j])
            }
        }
        return w
    }
    
